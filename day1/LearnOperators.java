package week1.day1;

public class LearnOperators {
	public static void main(String[] args) {
		//arthmetic operators
		System.out.println(4+5);
		System.out.println(4-5);
		System.out.println(4*5);
		System.out.println(4/5);
		System.out.println(4%5);
		System.out.println(4<5);
		System.out.println(4>5);
		
		//assignment operators
		int a=5;
		int b = 4 ;
		b+=5;
		System.out.println("a"+a);
		System.out.println("b"+b);
		
	}

}
